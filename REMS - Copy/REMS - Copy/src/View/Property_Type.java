    public void fillTypeList()
    {
        p_type type=new p_type();
        
        HashMap<String, Integer> map = type.getTypesMap();
        
        DefaultListModel listModel = new DefaultListModel ();
        
        int i=0;
        
        for(String typeName:map.keySet())
        {
            listModel.add(i,typeName);
            i++;
        }
        list_type.setModel(listModel);
        
    }
    
    
    
    private void btn_delete_typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete_typeActionPerformed
         //Delete type
         
         try
         {
             Integer id =Integer.valueOf(txt_id.getText()); 
      
       
        p_type type =new p_type(id,"","");
        
        if(!txt_id.getText().trim().equals(""))// if the type id is not empty
        {
            //Show confirmation message before deleting
            int yes_or_no= JOptionPane.showConfirmDialog(null, "DO YOU WANT TO DELETE THIS TYPE ?","DELETE TYPE",JOptionPane.YES_NO_OPTION);
            if(yes_or_no==JOptionPane.YES_OPTION)
            {
                if(type.execTypeQuery("delete",type))
        {
            JOptionPane.showMessageDialog(null,"TYPE DELETED","DELETE TYPE",1);
            fillTypeList();
            txt_description.setText("");
            txt_id.setText("");
            txt_name.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"OPERATION FAILED","DELETE TYPE",2);
        }
            }
            
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "ENTER TYPE NAME","EMPTY NAME",2);
        }
         }
         catch(Exception ex)
                 {
                   JOptionPane.showMessageDialog(null, ex.getMessage()+"ENTER VALID TYPE ID","INVALID ID",2);

                 }
    }//GEN-LAST:event_btn_delete_typeActionPerformed

    private void list_typeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_typeMouseClicked
        p_type type =new p_type();
        HashMap<String, Integer> map=type.getTypesMap();
        Integer typeId = map.get(list_type.getSelectedValue());
        type = type.getTypeById(typeId); 
        txt_id.setText(type.getId().toString());
        txt_name.setText(type.getName());
        txt_description.setText(type.getDescription());
    }//GEN-LAST:event_list_typeMouseClicked

