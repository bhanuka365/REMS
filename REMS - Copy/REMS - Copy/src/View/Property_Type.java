    private void btn_add_typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_typeActionPerformed
      
        //add new type
        
       String name=txt_name.getText();
       String description = txt_description.getText();
       
        p_type type =new p_type(0,name,description);
        
        if(!name.trim().equals(""))// if the type name is not empty
        {
            if(type.execTypeQuery("add",type))
        {
            JOptionPane.showMessageDialog(null,"NEW TYPE ADDED","ADD TYPE",1);
            fillTypeList();
            txt_description.setText("");
            txt_id.setText("");
            txt_name.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"OPERATION FAILED","ADD TYPE",2);
        }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "ENTER TYPE NAME","EMPTY NAME",2);
        }
        
        
        
        
       
    }//GEN-LAST:event_btn_add_typeActionPerformed

